package com.honpe.pojo;

public class OfferDetail {
	private String 零件编码;
	private String 数量;
	private String 报价单价;
	private String 交货日期;
	private String 备注;
	private String 税率;

	public String get零件编码() {
		return this.零件编码;
	}

	public void set零件编码(String 零件编码) {
		this.零件编码 = 零件编码;
	}

	public String get数量() {
		return this.数量;
	}

	public void set数量(String 数量) {
		this.数量 = 数量;
	}

	public String get报价单价() {
		return this.报价单价;
	}

	public void set报价单价(String 报价单价) {
		this.报价单价 = 报价单价;
	}

	public String get交货日期() {
		return this.交货日期;
	}

	public void set交货日期(String 交货日期) {
		this.交货日期 = 交货日期;
	}

	public String get备注() {
		return this.备注;
	}

	public void set备注(String 备注) {
		this.备注 = 备注;
	}

	public String get税率() {
		return this.税率;
	}

	public void set税率(String 税率) {
		this.税率 = 税率;
	}
}
